public class AreaCircumferenceCircle {
    public static void main(String[] args){
      double r = 5;

      double a = 3.14*r*r;
      System.out.println("Area of circle with radius " + r + " is " + a);

      double c = 2*3.14*r;
      System.out.println("Circumference of circle with radius " + r + " is " + c);

    }
    
}
